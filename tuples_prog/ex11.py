# Lists in Tuple

# Initial list
Input1 = ([1, 2, 3, 4], [5, 6, 7, 8])
Input2 = ([1, 2], [3, 4], [5, 6])
Input3 = ([9, 8, 7, 6, 5, 4, 3, 2, 1], [1, 2, 3])

# Using len to find no of list in tuple
Output1 = len(Input1)
Output2 = len(Input2)
Output3 = len(Input3)

# Printing
print("Initial list :")
print(Input1)
print("No of list in tuples are :")
print(Output1)

print("Initial list :")
print(Input2)
print("No of list in tuples are :")
print(Output2)

print("Initial list :")
print(Input3)
